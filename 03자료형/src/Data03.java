// 예제
// 정수를 저장하는 변수 메모리
public class Data03 {

	public static void main(String[] args) {
		/*
		 	데이터를 저장하기 위해서 변수 선언을 먼저 해야함
		 		자료형 변수명 = 값;
		 		
		 	변수명 규칙
		 	1. a~z, A~Z, 0~9, _ 으로 변수명을 정할 수 있음
		 	2. 미리 정해진 예약어를 사용할 수 없음(int, class, static 등)
		 	3. 사용자가 식별할 수 있는 의미 있는 이름으로 선언해야함
		 */
		int a;
		
		a = 1;
		
		System.out.println(a);
		
		a = 2;
		
		System.out.println(a);

	
		
	}
}
